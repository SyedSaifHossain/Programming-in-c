//Write a program using unary operator in programming in c.

#include<stdio.h>
int main()
{
    int x;
    printf("Enter the value of x : ");
    scanf("%d",&x);
    printf("Result x++ = %d\n",x++);
    printf("Result x = %d\n",x);
    printf("Result ++x = %d\n",++x);
    printf("Result x = %d\n",x);
    printf("Result x-- = %d\n",x--);
    printf("Result x = %d\n",x);
    printf("Result --x = %d\n",--x);
    printf("Result x = %d\n",x);
    return 0;
}


